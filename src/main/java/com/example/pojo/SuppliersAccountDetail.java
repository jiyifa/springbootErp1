package com.example.pojo;


public class SuppliersAccountDetail {

  private String dtypeid;
  private String oddNumbers;
  private String id;
  private String supplierdid;
  private java.sql.Date time;
  private String primaryMetals;
  private String presentMetals;


  public String getDtypeid() {
    return dtypeid;
  }

  public void setDtypeid(String dtypeid) {
    this.dtypeid = dtypeid;
  }


  public String getOddNumbers() {
    return oddNumbers;
  }

  public void setOddNumbers(String oddNumbers) {
    this.oddNumbers = oddNumbers;
  }


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getSupplierdid() {
    return supplierdid;
  }

  public void setSupplierdid(String supplierdid) {
    this.supplierdid = supplierdid;
  }


  public java.sql.Date getTime() {
    return time;
  }

  public void setTime(java.sql.Date time) {
    this.time = time;
  }


  public String getPrimaryMetals() {
    return primaryMetals;
  }

  public void setPrimaryMetals(String primaryMetals) {
    this.primaryMetals = primaryMetals;
  }


  public String getPresentMetals() {
    return presentMetals;
  }

  public void setPresentMetals(String presentMetals) {
    this.presentMetals = presentMetals;
  }

}
