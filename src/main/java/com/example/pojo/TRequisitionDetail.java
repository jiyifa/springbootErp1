package com.example.pojo;


public class TRequisitionDetail {

  private String billno;
  private String linid;
  private String prodid;
  private String prodname;
  private String prodsize;
  private String stdunitname;
  private String sums;
  private String lotNumber;
  private String remark;


  public String getBillno() {
    return billno;
  }

  public void setBillno(String billno) {
    this.billno = billno;
  }


  public String getLinid() {
    return linid;
  }

  public void setLinid(String linid) {
    this.linid = linid;
  }


  public String getProdid() {
    return prodid;
  }

  public void setProdid(String prodid) {
    this.prodid = prodid;
  }


  public String getProdname() {
    return prodname;
  }

  public void setProdname(String prodname) {
    this.prodname = prodname;
  }


  public String getProdsize() {
    return prodsize;
  }

  public void setProdsize(String prodsize) {
    this.prodsize = prodsize;
  }


  public String getStdunitname() {
    return stdunitname;
  }

  public void setStdunitname(String stdunitname) {
    this.stdunitname = stdunitname;
  }


  public String getSums() {
    return sums;
  }

  public void setSums(String sums) {
    this.sums = sums;
  }


  public String getLotNumber() {
    return lotNumber;
  }

  public void setLotNumber(String lotNumber) {
    this.lotNumber = lotNumber;
  }


  public String getRemark() {
    return remark;
  }

  public void setRemark(String remark) {
    this.remark = remark;
  }

}
