package com.example.pojo;


public class SuppliersAddress {

  private String said;
  private String sid;
  private String address;
  private String remarks;
  private String zipCode;
  private String contacts;
  private String contactsTitles;
  private String telephone;
  private String portraiture;
  private String route;


  public String getSaid() {
    return said;
  }

  public void setSaid(String said) {
    this.said = said;
  }


  public String getSid() {
    return sid;
  }

  public void setSid(String sid) {
    this.sid = sid;
  }


  public String getAddress() {
    return address;
  }

  public void setAddress(String address) {
    this.address = address;
  }


  public String getRemarks() {
    return remarks;
  }

  public void setRemarks(String remarks) {
    this.remarks = remarks;
  }


  public String getZipCode() {
    return zipCode;
  }

  public void setZipCode(String zipCode) {
    this.zipCode = zipCode;
  }


  public String getContacts() {
    return contacts;
  }

  public void setContacts(String contacts) {
    this.contacts = contacts;
  }


  public String getContactsTitles() {
    return contactsTitles;
  }

  public void setContactsTitles(String contactsTitles) {
    this.contactsTitles = contactsTitles;
  }


  public String getTelephone() {
    return telephone;
  }

  public void setTelephone(String telephone) {
    this.telephone = telephone;
  }


  public String getPortraiture() {
    return portraiture;
  }

  public void setPortraiture(String portraiture) {
    this.portraiture = portraiture;
  }


  public String getRoute() {
    return route;
  }

  public void setRoute(String route) {
    this.route = route;
  }

}
